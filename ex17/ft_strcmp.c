int	ft_strcmp(char *s1, char *s2)
{
	int	a;
	int	b;

	a = 0;
	b = 0;
	while (s1[a] != '\0' && s2[b] != '\0' && s1[a] == s2[b])
	{
		a++;
		b++;
	}
	return (s1[a] - s2[b]);
}
